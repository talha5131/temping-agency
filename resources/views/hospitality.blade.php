							waiters will have to wear masks and use hand sanitiser before serving each table;
							recirculating air in an enclosed space through ventilation systems is recognised as a health risk, and should therefore be avoided.</p>
						<p>Keeping windows open is advised instead;
							where possible, outdoor seating will be encouraged;
							self-service buffets are not allowed;
